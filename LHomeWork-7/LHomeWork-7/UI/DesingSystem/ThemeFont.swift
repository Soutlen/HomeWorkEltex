import UIKit

enum ThemeFont {
    static func regular(size: CGFloat) -> UIFont {
        UIFont.systemFont(ofSize: size)
    }
    
    static func medium(size: CGFloat) -> UIFont {
        UIFont.systemFont(ofSize: size)
    }
    
    static func bold(size: CGFloat) -> UIFont {
        UIFont.systemFont(ofSize: size)
    }
}
